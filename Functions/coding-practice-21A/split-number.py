# Read the space-separated numbers from user input
numbers = input().split()

# Convert the numbers from strings to integers
numbers = [int(num) for num in numbers]

# Print the list of numbers
print(numbers)

#  The code starts by reading the user input.
#  The input is a list of numbers separated by spaces.
#  The code then splits the list into individual numbers and converts them to integers.
#  Finally, it prints out the list of numbers that were read from the user input.
#  The code will print the list of numbers.